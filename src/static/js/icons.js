export function initializeIcons() {
  // Topbar icons
  const topbarIcon = document.getElementById('topbar-icon');
  if (topbarIcon) {
    topbarIcon.innerHTML = createIcon('arpvs', 16);
  }

  const reloadBtn = document.getElementById('reload-btn');
  if (reloadBtn) {
    reloadBtn.innerHTML = createIcon('reload', 16);
  }

  // Player icons
  const playerArtIcon = document.getElementById('player-art-icon');
  if (playerArtIcon) {
    playerArtIcon.innerHTML = createIcon('music', 20);
  }

  const btnPrev = document.getElementById('btn-prev');
  if (btnPrev) btnPrev.innerHTML = createIcon('prev', 18);

  const btnPlay = document.getElementById('btn-play');
  if (btnPlay) btnPlay.innerHTML = createIcon('play', 18);

  const btnNext = document.getElementById('btn-next');
  if (btnNext) btnNext.innerHTML = createIcon('next', 18);

  const btnShuffle = document.getElementById('btn-shuffle');
  if (btnShuffle) btnShuffle.innerHTML = createIcon('shuffle', 18);

  const btnLoop = document.getElementById('btn-loop');
  if (btnLoop) btnLoop.innerHTML = createIcon('loop', 18);

  // Splash icon
  const splashIcon = document.getElementById('splash-icon');
  if (splashIcon) {
    splashIcon.innerHTML = createIcon('arpvs', 24);
  }
}
